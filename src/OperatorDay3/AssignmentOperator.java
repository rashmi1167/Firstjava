package OperatorDay3;

public class AssignmentOperator {

	public static void main(String[] args) 
	
	{
		// =,+=,-=,*=,/=,%=
		
		int a=5;
		//a=a+5;
		a+=5;
		System.out.println(a);
		a-=2;
		System.out.println(a);
		a*=2;
		System.out.println(a);
		a/=4;
		System.out.println(a);
		a%=7;
		System.out.println(a);
	}

}
