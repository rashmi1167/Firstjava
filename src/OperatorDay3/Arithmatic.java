package OperatorDay3;

public class Arithmatic {

	public static void main(String[] args)
	
	{
		// Arethmatic operators are +, -, *, /, %
		
		int a=10;
		int b=3;
		
		System.out.println("Sum of a and b is " + (a+b)); // 13
		
		System.out.println("Substraction of a and b is " + (a-b)); // 7

		System.out.println("Multiplication of a and b is " + (a*b)); //30
		
		System.out.println("Division of a and b is " + (a/b)); // 3
		
		System.out.println("Modulo division of a and b is " + (a%b)); // 1
		

	}

}
